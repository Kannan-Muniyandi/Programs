package com.subpractice.org;

public class Prime_Number_While {
	
	public static void main(String[] args) {
		int i=2,n=23;
		while(i<=2) {
			
			if(n%2==0) {
				System.out.println(n+ " is not a prime number");
			}else {
				System.out.println(n+ " is a prime number");
				break;
			}
			
			
			
		}
		
		
	}

}
